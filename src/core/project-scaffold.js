const path = require("path");
const { createDefaultConfig, saveConfig } = require("./config");
const {
  ensureDir,
  isDirectoryEmpty,
  pathExists,
  writeFile,
  assertSafeProjectTarget,
} = require("../utils/files");

async function scaffoldProject(projectName, baseDirectory) {
  assertSafeProjectTarget(projectName);

  const projectDirectory = path.resolve(baseDirectory, projectName);
  const projectExists = await pathExists(projectDirectory);

  if (projectExists) {
    const empty = await isDirectoryEmpty(projectDirectory);

    if (!empty) {
      throw new Error(`Target directory already exists and is not empty: ${projectName}`);
    }
  } else {
    await ensureDir(projectDirectory);
  }

  const createdDirectories = [];
  const createdFiles = [];

  const relativeProjectPath = projectName;
  trackDirectory(createdDirectories, relativeProjectPath);

  const directories = [
    "backend",
    "backend/src",
    "backend/src/routes",
    "frontend",
    "frontend/src",
    "frontend/public",
  ];

  for (const directory of directories) {
    const absoluteDirectory = path.join(projectDirectory, directory);
    await ensureDir(absoluteDirectory);
    trackDirectory(createdDirectories, path.join(relativeProjectPath, directory));
  }

  const files = buildProjectFiles(projectName);

  saveConfig(projectDirectory, createDefaultConfig(projectName));
  createdFiles.push(path.join(relativeProjectPath, "kibs.config.json"));

  for (const [relativeFilePath, content] of Object.entries(files)) {
    await writeFile(path.join(projectDirectory, relativeFilePath), content);
    createdFiles.push(path.join(relativeProjectPath, relativeFilePath));
  }

  return {
    projectPath: projectDirectory,
    relativeProjectPath,
    createdDirectories,
    createdFiles,
  };
}

function trackDirectory(createdDirectories, relativePath) {
  if (!createdDirectories.includes(relativePath)) {
    createdDirectories.push(relativePath);
  }
}

function buildProjectFiles(projectName) {
  return {
    "README.md": buildProjectReadme(projectName),
    "backend/package.json": `${JSON.stringify(buildBackendPackageJson(projectName), null, 2)}\n`,
    "backend/.gitignore": "node_modules/\n.env\n",
    "backend/src/app.js": buildBackendAppFile(),
    "backend/src/server.js": buildBackendServerFile(),
    "backend/src/routes/index.js": buildBackendRouteFile(projectName),
    "frontend/package.json": `${JSON.stringify(buildFrontendPackageJson(projectName), null, 2)}\n`,
    "frontend/.gitignore": "node_modules/\n",
    "frontend/public/index.html": buildFrontendHtml(projectName),
    "frontend/src/app.js": buildFrontendAppFile(projectName),
    "frontend/src/main.js": buildFrontendMainFile(),
    "frontend/src/styles.css": buildFrontendStylesFile(),
  };
}

function buildBackendPackageJson(projectName) {
  return {
    name: `${projectName}-backend`,
    private: true,
    version: "0.1.0",
    main: "src/server.js",
    scripts: {
      start: "node src/server.js",
      dev: "node src/server.js",
    },
    dependencies: {
      express: "^5.1.0",
    },
  };
}

function buildFrontendPackageJson(projectName) {
  return {
    name: `${projectName}-frontend`,
    private: true,
    version: "0.1.0",
    description: "Frontend starter shell for a Kibs-generated React + Tailwind app",
  };
}

function buildProjectReadme(projectName) {
  return `# ${projectName}

This project was generated by Kibs CLI.

## Structure

- \`kibs.config.json\`: Kibs v1 source of truth
- \`backend/\`: Express starter application
- \`frontend/\`: React + Tailwind-oriented starter structure

## Backend

\`\`\`powershell
cd backend
npm install
npm start
\`\`\`

The backend starts on port \`3001\` by default.

## Frontend

The frontend is a starter shell prepared for React + Tailwind integration in the next Kibs generation steps. It intentionally does not include a frontend scaffolding framework in this initial foundation.
`;
}

function buildBackendAppFile() {
  return `const express = require("express");
const indexRouter = require("./routes");

const app = express();

app.use(express.json());
app.use("/", indexRouter);

module.exports = app;
`;
}

function buildBackendServerFile() {
  return `const app = require("./app");

const port = process.env.PORT || 3001;

app.listen(port, () => {
  console.log(\`Kibs backend listening on port \${port}\`);
});
`;
}

function buildBackendRouteFile(projectName) {
  return `const express = require("express");

const router = express.Router();

router.get("/", (_request, response) => {
  response.json({
    product: "Kibs",
    project: ${JSON.stringify(projectName)},
    backend: "express",
    status: "ok"
  });
});

module.exports = router;
`;
}

function buildFrontendHtml(projectName) {
  return `<!DOCTYPE html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>${projectName}</title>
    <link rel="stylesheet" href="../src/styles.css" />
  </head>
  <body>
    <div id="app"></div>
    <script src="../src/main.js"></script>
  </body>
</html>
`;
}

function buildFrontendAppFile(projectName) {
  return `function App() {
  return [
    "<main class=\\"app-shell\\">",
    "  <section class=\\"hero\\">",
    "    <p class=\\"eyebrow\\">Kibs CLI starter</p>",
    "    <h1>${projectName}</h1>",
    "    <p>A React + Tailwind-oriented frontend starter generated by Kibs.</p>",
    "  </section>",
    "</main>"
  ].join("\\n");
}

module.exports = App;
`;
}

function buildFrontendMainFile() {
  return `const App = require("./app");

const root = document.getElementById("app");

if (root) {
  root.innerHTML = App();
}
`;
}

